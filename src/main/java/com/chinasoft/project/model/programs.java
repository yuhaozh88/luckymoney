package com.chinasoft.project.model;

public class programs {
	int pid;
	String program_name;
	String dept_name;
	int money_get;
	boolean is_live;
	int program_order;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getProgram_name() {
		return program_name;
	}
	public void setProgram_name(String program_name) {
		this.program_name = program_name;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}
	public int getMoney_get() {
		return money_get;
	}
	public void setMoney_get(int money_get) {
		this.money_get = money_get;
	}
	public boolean isIs_live() {
		return is_live;
	}
	public void setIs_live(boolean is_live) {
		this.is_live = is_live;
	}
	public int getProgram_order() {
		return program_order;
	}
	public void setProgram_order(int program_order) {
		this.program_order = program_order;
	}
}
